    <script>
        // Back to Top Button with smooth scroll
        const backToTopButton = document.getElementById('back-to-top');
        window.addEventListener('scroll', () => {
            if (window.pageYOffset > 300) {
                backToTopButton.classList.remove('hidden');
                backToTopButton.classList.add('scale-in');
            } else {
                backToTopButton.classList.add('hidden');
            }
        });
        
        backToTopButton.addEventListener('click', () => {
            window.scrollTo({ 
                top: 0, 
                behavior: 'smooth' 
            });
        });

        // Loading indicator
        const loadingIndicator = document.getElementById('loading-indicator');
        const loadingBar = loadingIndicator.querySelector('div');
        
        window.addEventListener('scroll', () => {
            const scrollTop = window.pageYOffset;
            const docHeight = document.body.offsetHeight - window.innerHeight;
            const scrollPercent = (scrollTop / docHeight) * 100;
            loadingBar.style.width = scrollPercent + '%';
        });

        // Newsletter form enhancement
        const newsletterForm = document.querySelector('form');
        if (newsletterForm) {
            newsletterForm.addEventListener('submit', (e) => {
                e.preventDefault();
                const email = newsletterForm.querySelector('input[type="email"]').value;
                if (email) {
                    // Add success animation
                    const button = newsletterForm.querySelector('button');
                    const originalText = button.innerHTML;
                    button.innerHTML = '<i class="fa-solid fa-check mr-2"></i>Subscribed!';
                    button.classList.add('bg-green-600');
                    button.classList.remove('bg-white', 'text-blue-700');
                    
                    setTimeout(() => {
                        button.innerHTML = originalText;
                        button.classList.remove('bg-green-600');
                        button.classList.add('bg-white', 'text-blue-700');
                        newsletterForm.reset();
                    }, 3000);
                }
            });
        }

        // Add fade-in animation to footer sections
        const observerOptions = {
            threshold: 0.1,
            rootMargin: '0px 0px -50px 0px'
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('fade-in');
                }
            });
        }, observerOptions);

        // Observe footer sections
        document.querySelectorAll('footer > div > div > div').forEach(section => {
            observer.observe(section);
        });

        // Smooth hover effects for social links
        document.querySelectorAll('footer a[href="#"]').forEach(link => {
            link.addEventListener('mouseenter', function() {
                this.style.transform = 'translateY(-2px)';
            });
            
            link.addEventListener('mouseleave', function() {
                this.style.transform = 'translateY(0)';
            });
        });
    </script>
